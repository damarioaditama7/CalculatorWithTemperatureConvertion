    private void btnBackspaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackspaceActionPerformed
        int length = fieldNumber.getText().length();
        int number = fieldNumber.getText().length() - 1;
        String store;
        if (length > 0) {
            StringBuilder backspace = new StringBuilder(fieldNumber.getText());
            backspace.deleteCharAt(number);
            store = backspace.toString();
            fieldNumber.setText(store);

        }
    }//GEN-LAST:event_btnBackspaceActionPerformed

