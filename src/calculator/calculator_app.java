    public void calculations() {
        switch (calculation) {
            case 1:
                answer = number + Double.parseDouble(fieldNumber.getText());
                fieldNumber.setText(Double.toString(answer));
                break;
            case 2:
                answer = number - Double.parseDouble(fieldNumber.getText());
                fieldNumber.setText(Double.toString(answer));
                break;
            case 3:
                answer = number * Double.parseDouble(fieldNumber.getText());
                fieldNumber.setText(Double.toString(answer));
                break;
            case 4:
                answer = number / Double.parseDouble(fieldNumber.getText());
                fieldNumber.setText(Double.toString(answer));
                break;
            case 5:
                answer = (number * 9.0) / 5.0 + 32.0;
                fieldNumber.setText(Double.toString(answer));
                break;
            case 6:
                answer = number + 273.15;
                fieldNumber.setText(Double.toString(answer));
                break;
            case 7:
                answer = ((number - 32.0) * 5.0) / 9.0;
                fieldNumber.setText(Double.toString(answer));
                break;
            case 8:
                answer = (((number - 32.0) * 5.0) / 9.0) + 273.15;
                fieldNumber.setText(Double.toString(answer));
                break;
            case 9:
                answer = number - 273.15;
                fieldNumber.setText(Double.toString(answer));
                break;
            case 10:
                answer = ((number - 273.15) * 9.0) / 5.0 + 32.0;
                fieldNumber.setText(Double.toString(answer));
                break;
        }
    }
